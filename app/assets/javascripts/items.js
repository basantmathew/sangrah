var Item = function () {

  var initForm = function () {
    $(document).on('nested:fieldAdded:item_creators', function(event){
      event.field.find('.selectize').selectize();
    });

    $('#item_tags').selectize({
        delimiter: ',',
        persist: false,
        create: function(input) {
            return {
                value: input,
                text: input
            }
        }
    });
  };

  return {
    index: function () {},

    new: function () {
      initForm();
    },

    create: function () {
      initForm();
    },

    edit: function () {
      initForm();
    },

    update: function () {
      initForm();
    },

    show: function () {},

    init: function () {
    }
  };

}();
